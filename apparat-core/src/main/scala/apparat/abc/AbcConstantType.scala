package apparat.abc

object AbcConstantType {
	val Int = 0x03
	val UInt = 0x04
	val Double = 0x06
	val Utf8 = 0x01
	val True = 0x0b
	val False = 0x0a
	val Null = 0x0c
	val Undefined = 0x00
	val Namespace = 0x08
	val PackageNamespace = 0x16
	val InternalNamespace = 0x17
	val ProtectedNamespace = 0x18
	val ExplicitNamespace = 0x19
	val StaticProtectedNamespace = 0x1a
	val PrivateNamespace = 0x05
}
